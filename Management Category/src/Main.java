    private void loadData()
    {
        ArrayList<Category> list = cdb.getAllCategories();
        tm.setRowCount(0);
        for (Category category : list)
        {
            tm.addRow(category.toObject());
        }
        jLabel1.setText("The number of categories: " + tm.getRowCount());
    }
    
    private void loadData(ArrayList<Category> list)
    {
        tm.setRowCount(0);
        for (Category category : list)
        {
            tm.addRow(category.toObject());
        }
        jLabel1.setText("The number of categories: " + tm.getRowCount());
    }

