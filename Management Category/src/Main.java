    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int row = jTable1.getSelectedRow();
        if(row  < 0 || row > tm.getRowCount())
            JOptionPane.showMessageDialog(this, "select a row to delete");
        else
        {
            Category c = new Category(tm.getValueAt(row, 0).toString(), tm.getValueAt(row, 1).toString(), tm.getValueAt(row, 2).toString());
            cdb.delete(c);
            loadData();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = jTextField1.getText().trim();
        String name = jTextField2.getText().trim();
        String describe = jTextField3.getText().trim();
        if(cdb.exsitedCategory(id))
        {
            JOptionPane.showMessageDialog(this, id + " exsited!!!");
            jTextField1.requestFocus();
            return;
        }
        else
        {
            cdb.add(new Category(id, name, describe));
            loadData();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        jTextField1.setText(tm.getValueAt(row, 0).toString());
        jTextField2.setText(tm.getValueAt(row, 1).toString());
        jTextField3.setText(tm.getValueAt(row, 2).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int row = jTable1.getSelectedRow();
        if(row < 0 || row > tm.getRowCount())
            JOptionPane.showMessageDialog(this, "select a row to edit");
        else
        {
            Category c = new Category(jTextField1.getText(), jTextField2.getText(), jTextField3.getText());
            cdb.update(c);
            loadData();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int select = jComboBox1.getSelectedIndex();
        ArrayList<Category> list = new ArrayList<>();
        if(select == 0)
        {
            list = cdb.sortByID();
            loadData(list);
        }
        if(select == 1) 
        {
            list = cdb.sortByName();
            loadData(list);
        }
        if(select == 2) 
        {
            list = cdb.sortByDescribe();
            loadData(list);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int select = jComboBox2.getSelectedIndex();
        ArrayList<Category> list = new ArrayList<>();
        if(select == 0)
        {
            list = cdb.search("id", jTextField4.getText());
            loadData(list);
        }
        if(select == 1) 
        {
            list = cdb.search("name", jTextField4.getText());
            loadData(list);
        }
        if(select == 2) 
        {
            list = cdb.search("describe", jTextField4.getText());
            loadData(list);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

